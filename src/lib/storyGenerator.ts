export interface StoryParams {
  name: string;
  age: string;
  hero: string;
  theme: string;
  wish?: string;
}

const SETTINGS: Record<string, string> = {
  'Приключения': 'на краю Загадочного леса, где каждая тропинка хранит сюрприз',
  'Волшебство': 'в сияющем городе, где облака сделаны из сахарной ваты',
  'Космос и звёзды': 'на маленькой планете среди мерцающих звёзд',
  'Дружба': 'в уютной деревушке у Тёплого ручья',
  'Подводный мир': 'на дне ласкового бирюзового моря',
  'Динозавры': 'в Долине, где ещё гуляли добрые динозавры',
};

const QUESTS: Record<string, string> = {
  'Приключения': 'найти спрятанный сундук с волшебными красками',
  'Волшебство': 'вернуть городу его потерянную улыбку',
  'Космос и звёзды': 'зажечь самую далёкую погасшую звезду',
  'Дружба': 'помирить поссорившихся жителей деревни',
  'Подводный мир': 'отыскать потерянную жемчужину русалочки',
  'Динозавры': 'проводить домой заблудившегося динозаврёнка',
};

export function generateStory(p: StoryParams): { title: string; paragraphs: string[] } {
  const name = p.name.trim() || 'Малыш';
  const age = p.age.trim();
  const hero = p.hero || 'отважный герой';
  const theme = p.theme || 'Приключения';
  const place = SETTINGS[theme] ?? SETTINGS['Приключения'];
  const quest = QUESTS[theme] ?? QUESTS['Приключения'];
  const ageWord = age ? `${age}-летн${Number(age) === 1 ? 'ий' : 'ий'}` : 'маленький';

  const title = `${name} и ${theme.toLowerCase()}`;

  const wishLine = p.wish?.trim()
    ? `А ещё ${name} ни на минуту не забывал${name.endsWith('а') ? 'а' : ''} про то, что больше всего на свете любит: ${p.wish.trim()}. И это придало смелости.`
    : `И хотя сердечко немного волновалось, ${name} знал${name.endsWith('а') ? 'а' : ''}, что справится.`;

  const paragraphs = [
    `Жил-был ${ageWord} ${name}. Однажды утром, ${place}, случилось кое-что необычное.`,
    `Прямо у окна появился ${hero.toLowerCase()} и прошептал: «${name}, ты нам очень нужн${name.endsWith('а') ? 'а' : ''}! Только ты можешь ${quest}». ${name} тут же согласил${name.endsWith('а') ? 'ась' : 'ся'} помочь.`,
    `Путь был непростым: то ветер кружил листья, то загадочные огоньки манили в сторону. ${wishLine}`,
    `И вот, благодаря доброте и смелости, ${name} справил${name.endsWith('а') ? 'ась' : 'ся'} с заданием! ${hero} обнял${'и'} нового друга, а вокруг засияли тёплые искорки радости.`,
    `С тех пор все вокруг знали: ${name} — самый настоящий герой. А вечером, засыпая, ${name} улыбал${name.endsWith('а') ? 'ась' : 'ся'}, ведь впереди ждало ещё много чудесных приключений. Тут и сказке конец. 🌙`,
  ];

  return { title, paragraphs };
}
