const fe = (name: string) => name.endsWith('а') || name.endsWith('я');

export function generateStory(p: StoryParams): { title: string; paragraphs: string[] } {
  const name = p.name.trim() || 'Малыш';
  const age = p.age.trim();
  const hero = p.hero || 'отважный герой';
  const theme = p.theme || 'Приключения';
  const pages = Math.min(Math.max(p.pages ?? 5, 3), 10);
  const place = SETTINGS[theme] ?? SETTINGS['Приключения'];
  const quest = QUESTS[theme] ?? QUESTS['Приключения'];
  const ageWord = age ? `${age}-летн${fe(name) ? 'яя' : 'ий'}` : 'маленький';
  const middles = (MIDDLE_PARAGRAPHS[theme] ?? MIDDLE_PARAGRAPHS['Приключения']).slice();
  const title = `${name} и ${theme.toLowerCase()}`;
  const wishLine = p.wish?.trim()
    ? `А ещё ${name} ни на минуту не забывал${fe(name) ? 'а' : ''} про то, что больше всего на свете любит: ${p.wish.trim()}. И это придало смелости.`
    : `И хотя сердечко немного волновалось, ${name} знал${fe(name) ? 'а' : ''}, что справится.`;

  const fixed = [
    `Жил-был${fe(name) ? 'а' : ''} ${ageWord} ${name}. Однажды утром, ${place}, случилось кое-что необычное.`,
    `Прямо у окна появился ${hero.toLowerCase()} и прошептал: «${name}, ты нам очень нужн${fe(name) ? 'а' : ''}! Только ты можешь ${quest}». ${name} тут же согласил${fe(name) ? 'ась' : 'ся'} помочь.`,
    `Путь был непростым: то ветер кружил листья, то загадочные огоньки манили в сторону. ${wishLine}`,
  ];
  const ending = `С тех пор все вокруг знали: ${name} — самый настоящий герой. А вечером, засыпая, ${name} улыбал${fe(name) ? 'ась' : 'ся'}, ведь впереди ждало ещё много чудесных приключений. Тут и сказке конец. 🌙`;
  const penultimate = `И вот, благодаря доброте и смелости, ${name} справил${fe(name) ? 'ась' : 'ся'} с заданием! ${hero} обнял${fe(name) ? 'и' : 'и'} нового друга, а вокруг засияли тёплые искорки радости.`;

  const middleCount = pages - fixed.length - 2;
  const chosenMiddles = middles.slice(0, Math.max(0, middleCount));

  const paragraphs = [...fixed, ...chosenMiddles, penultimate, ending].slice(0, pages);

  return { title, paragraphs };
}
